export function AdminSidebar({ isSuperAdmin, affiliatesEnabled = true }: { isSuperAdmin: boolean; affiliatesEnabled?: boolean }) {
  const pathname = usePathname()

  return (
    <aside className="fixed inset-y-0 left-0 z-30 flex w-64 flex-col" style={{ backgroundColor: "var(--sidebar-bg)" }}>
      {/* Logo */}
      <div className="flex h-16 items-center gap-2 border-b border-white/10 px-6">
        <div className="flex h-8 w-8 items-center justify-center rounded-lg" style={{ backgroundColor: "#845adf" }}>
          <svg className="h-4 w-4 text-white" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
            <path strokeLinecap="round" strokeLinejoin="round" d="M13 10V3L4 14h7v7l9-11h-7z" />
          </svg>
        </div>
        <span className="text-lg font-bold text-white">Admin</span>
        <Link
          href="/"
          className="ml-auto text-[11px] text-white/50 transition-colors hover:text-white/80"
        >
          View Store
        </Link>
      </div>

      {/* Navigation */}
      <nav className="flex-1 overflow-y-auto px-4 py-4">
        {navSections.map((section) => {
          const visibleItems = section.items.filter((item) => {
            if (item.superAdminOnly && !isSuperAdmin) return false
            if (item.requiresAffiliates && !affiliatesEnabled) return false
            return true
          })

          if (visibleItems.length === 0) return null

          return (
            <div key={section.title} className="mb-4">
              <p
                className="mb-2 px-3 text-[10px] font-bold uppercase tracking-widest"
                style={{ color: "rgba(255,255,255,0.35)" }}
              >
                {section.title}
              </p>
              <div className="space-y-0.5">
                {visibleItems.map((item) => {
                  const isActive =
                    item.href === "/admin"
                      ? pathname === "/admin"
                      : item.href === "/admin/settings"
                        ? pathname === "/admin/settings"
                        : pathname.startsWith(item.href)

                  return (
                    <Link
                      key={item.href}
                      href={item.href}
                      className={cn(
                        "flex items-center gap-3 rounded-md px-3 py-2 text-[13px] font-medium transition-all duration-150",
                        isActive
                          ? "border-l-2"
                          : "border-l-2 border-transparent"
                      )}
                      style={
                        isActive
                          ? {
                              backgroundColor: "var(--sidebar-active-bg)",
                              color: "var(--sidebar-active-text)",
                              borderLeftColor: "var(--sidebar-active-text)",
                            }
                          : {
                              color: "var(--sidebar-text)",
                            }
                      }
                      onMouseEnter={(e) => {
                        if (!isActive) {
                          e.currentTarget.style.backgroundColor = "rgba(255,255,255,0.05)"
                          e.currentTarget.style.color = "rgba(255,255,255,0.95)"
                        }
                      }}
                      onMouseLeave={(e) => {
                        if (!isActive) {
                          e.currentTarget.style.backgroundColor = "transparent"
                          e.currentTarget.style.color = "var(--sidebar-text)"
                        }
                      }}
                    >
                      {icons[item.icon]}
                      {item.label}
                    </Link>
                  )
                })}
              </div>
            </div>
          )
        })}
      </nav>
    </aside>
  )
}
